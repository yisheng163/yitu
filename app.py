from log import logger
from get_key import get_api_key
# 获取 API Key
api_key = get_api_key()

# 检查是否成功获取到 API Key
if not api_key:
    raise ValueError("未找到API密钥。请在.env文件中设置MODELSCOPE_API_KEY值，或设置环境变量 'myKEY'。")

# ModelScope API 配置
base_url = 'https://api-inference.modelscope.cn/'
common_headers = {
    "Authorization": f"Bearer {api_key}",
    "Content-Type": "application/json",
}

# 推荐的常用宽高比
aspect_ratios = {
    "1:1": (1328, 1328),
    "16:9": (1664, 928),
    "9:16": (928, 1664),
    "4:3": (1472, 1140),
    "3:4": (1140, 1472),
    "3:2": (1584, 1056),
    "2:3": (1056, 1584),
}

def update_size(aspect_ratio):
    """根据选择的宽高比更新宽度和高度"""
    if aspect_ratio in aspect_ratios:
        return aspect_ratios[aspect_ratio]
    return 760, 1280

def generate_image(prompt, width=760, height=1280):
    """调用 Qwen/Qwen-Image API 并返回生成的图像"""
    try:
        # 记录用户请求日志
        logger.info(f"用户请求生成图像: 提示词='{prompt}', 宽度={width}, 高度={height}")
        
        # 提交图像生成任务
        response = requests.post(
            f"{base_url}v1/images/generations",
            headers={**common_headers, "X-ModelScope-Async-Mode": "true"},
            data=json.dumps({
                "model": "Qwen/Qwen-Image-2512",  # ModelScope Model-Id, required
                "prompt": prompt,
                "size": f"{width}x{height}"
                
            }, ensure_ascii=False).encode('utf-8')
        )        
        response.raise_for_status()
        task_id = response.json()["task_id"]
        
        # 记录任务提交成功日志
        logger.info(f"图像生成任务已提交: 任务ID={task_id}, 提示词='{prompt}'")        
        # 轮询任务状态直到完成
        while True:
            result = requests.get(
                f"{base_url}v1/tasks/{task_id}",
                headers={**common_headers, "X-ModelScope-Task-Type": "image_generation"},
            )
            result.raise_for_status()
            data = result.json()
            
            if data["task_status"] == "SUCCEED":
                # 下载并返回生成的图像
                image_url = data["output_images"][0]
                image_response = requests.get(image_url)
                image_response.raise_for_status()
                return Image.open(BytesIO(image_response.content))
            elif data["task_status"] == "FAILED":
                return f"图像生成失败: 任务执行失败"
            
            time.sleep(5)  # 等待5秒后再次检查
            
    except Exception as e:
        # 记录异常日志
        logger.error(f"图像生成过程中出现异常: 提示词='{prompt}', 错误信息={str(e)}")
        return f"图像生成失败: {str(e)}"
